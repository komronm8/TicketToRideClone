package entity

/**
 * The Color of a [WagonCard]/[Route]. [JOKER] stands for the color gray in the context of [Routes][Route]
 * and for a locomotive card in the context of [WagonCards][WagonCard]
 */
enum class Color {
    GREEN,
    ORANGE,
    YELLOW,
    RED,
    PURPLE,
    BLUE,
    WHITE,
    BLACK,
    JOKER,
}